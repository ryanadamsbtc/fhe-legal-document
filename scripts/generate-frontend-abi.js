#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

function main() {
  const deploymentsDir = path.join(__dirname, '..', 'deployments', 'sepolia');
  const targetJson = path.join(deploymentsDir, 'LegalDocs.json');
  if (!fs.existsSync(targetJson)) {
    console.error(`Missing deployments file: ${targetJson}`);
    process.exit(1);
  }
  const raw = JSON.parse(fs.readFileSync(targetJson, 'utf8'));
  const { abi, address } = raw;
  if (!abi || !address) {
    console.error('LegalDocs.json missing abi or address');
    process.exit(1);
  }
  const outDir = path.join(__dirname, '..', 'frontend', 'src', 'abi');
  fs.mkdirSync(outDir, { recursive: true });
  const outFile = path.join(outDir, 'legalDocs.ts');
  const content = `// auto-generated by scripts/generate-frontend-abi.js\nexport const legalDocsAbi = ${JSON.stringify(abi, null, 2)} as const;\nexport const legalDocsAddress = '${address}' as const;\n`;
  fs.writeFileSync(outFile, content);
  console.log(`Wrote ${outFile} with address ${address}`);
}

if (require.main === module) main();

